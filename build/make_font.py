# build/make_font.py — PixelMplus12 を取得し、アプリで使う文字だけにサブセット化して
# base64 woff2 を埋め込んだ src/font.css を生成する。
# 依存: fonttools, brotli（venv 等）。ネットワーク（初回のフォント取得）。
import os, glob, base64, subprocess, sys, urllib.request

BASE = os.path.dirname(__file__)
CACHE = os.path.join(BASE, "_fontcache")
SRC = os.path.join(BASE, "..", "src")
URLS = {
    "PixelMplus12-Regular.ttf": "https://github.com/itouhiro/PixelMplus/raw/master/PixelMplus12-Regular.ttf",
    "PixelMplus12-Bold.ttf":    "https://github.com/itouhiro/PixelMplus/raw/master/PixelMplus12-Bold.ttf",
}
UA = {"User-Agent": "todofuken-quiz-fontbuild/1.0"}

def ensure_ttf(name):
    p = os.path.join(CACHE, name)
    if not os.path.exists(p):
        os.makedirs(CACHE, exist_ok=True)
        req = urllib.request.Request(URLS[name], headers=UA)
        open(p, "wb").write(urllib.request.urlopen(req, timeout=120).read())
    return p

def collect_charset():
    chars = set()
    for f in glob.glob(os.path.join(SRC, "*.js")) + [os.path.join(BASE, "..", "index.html")]:
        chars |= set(open(f, encoding="utf-8").read())
    # 安全のため確実に必要な表示文字を明示追加（地方名・UI）
    for extra in ["エリア", "全国", "北海道", "東北", "関東", "中部甲信越", "関西",
                  "中国", "四国", "九州", "地方", "都道府県クイズ", "クレジット"]:
        chars |= set(extra)
    def is_emoji(c):
        o = ord(c)
        return ((0x1F000 <= o <= 0x1FAFF) or (0x2600 <= o <= 0x27BF)
                or o in (0x2764, 0x2728, 0x2934, 0x2935)
                or (0xFE00 <= o <= 0xFE0F))
    return sorted(c for c in chars if ord(c) >= 0x20 and ord(c) != 0x7F and not is_emoji(c))

def subset_to_b64(ttf, text):
    out = ttf + ".sub.woff2"
    txtfile = ttf + ".chars.txt"
    open(txtfile, "w", encoding="utf-8").write(text)
    pyft = os.path.join(os.path.dirname(sys.executable), "pyftsubset")
    if not os.path.exists(pyft): pyft = "pyftsubset"
    subprocess.run([pyft, ttf, "--text-file=" + txtfile, "--flavor=woff2",
                    "--output-file=" + out, "--layout-features=", "--no-hinting",
                    "--desubroutinize"], check=True)
    b = open(out, "rb").read()
    os.remove(out); os.remove(txtfile)
    return base64.b64encode(b).decode()

def main():
    text = "".join(collect_charset())
    reg = subset_to_b64(ensure_ttf("PixelMplus12-Regular.ttf"), text)
    bold = subset_to_b64(ensure_ttf("PixelMplus12-Bold.ttf"), text)
    css = ("/* AUTO-GENERATED by build/make_font.py — do not edit by hand */\n"
           "/* Font: PixelMplus12 (c) 2002-2013 M+ FONTS PROJECT, M+ FONT LICENSE */\n"
           "@font-face{font-family:'PX';font-weight:400;font-display:swap;"
           "src:url(data:font/woff2;base64," + reg + ") format('woff2');}\n"
           "@font-face{font-family:'PX';font-weight:700;font-display:swap;"
           "src:url(data:font/woff2;base64," + bold + ") format('woff2');}\n")
    open(os.path.join(SRC, "font.css"), "w", encoding="utf-8").write(css)
    print(f"wrote src/font.css: {len(text)} chars, {len(css.encode())//1024}KB")

if __name__ == "__main__":
    main()
