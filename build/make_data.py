SUFFIX = ("都","道","府","県")
def accept_forms(kanji):
    forms = {kanji}
    if kanji != "北海道":
        for s in SUFFIX:
            if kanji.endswith(s):
                forms.add(kanji[:-1])
    return sorted(forms, key=len, reverse=True)

